# variaveis no python "não tem tipo", são tipos dinamicos

variavel = 10 # começou sendo um inteiro
variavel = "virei texto" # agora e uma string
variavel = 4.4 # agora e um float
# e assim vai...

# quanto as atribuições em si, mais uma vez é bem semelhante as outras linguagens de programação

variavel = 10
variavel += 10
variavel += variavel
variavel -= 10
variavel *= 10
variavel /= 10
variavel %= 10

print(variavel)